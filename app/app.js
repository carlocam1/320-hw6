function loadData (){
    $.each(CAMP, function(idx, camp){
        $("#app").append(`<div id="${idx}" class="camp-holder">
        <h5>${camp.campTitle}</h5>
        <div class="camp-image">
          <img src="images/camps_thumb/${camp.campThumbing}" alt="${camp.campTitle}" />
        </div>
        <div class="brief-rating">
          <div class="brief-des">
            ${camp.campBriefDescription}
          </div>
          <div class="thumb-price">Price: ${camp.itemPrice}</div>
        </div>
      </div>`);
    });

    initListeners(); 
}

// import { changePageContent } from "../model/model.js";

function initListeners() {
    $(".bars").click(function(e){
        // console.log("clicked");
        $(".bars").toggleClass("active");
        $(".links").toggleClass("active");
    });

    $(".links a").click(function(e){
        $(".bars").toggleClass("active");
        $(".links").toggleClass("active");
        // $(".links").removeClass("active");
    });

    $(".camp-holder").click(function(e){
      let campIndex = e.currentTarget.id;
      console.log(campIndex);
      
      $("#app").html(`<div class="camp">
      <div class="camp-container">
        <div class="campImg">
          <img src="images/camp_full/${CAMP[campIndex].campFullImg}" alt="" />
        </div>
          <div class="campDes">
            <h2>${CAMP[campIndex].campTitle}</h2>
            ${CAMP[campIndex].campDescription}
            <div class="reviews">
              <img src="images/${CAMP[campIndex].itemReview}" alt="" />
            </div>  
            <div class="price">
              <h1>${CAMP[campIndex].itemPrice}</h1>
            </div>
            <div class="button">add to car</div>

        </div>   
      </div>  
      <div class="close button">CLOSE</div>  
    </div>`);

      addCloseListener();

    });
}

function addCloseListener(){
  $(".close").click(function(){
    $("#app").html("");
    loadData();
  });
}


$(document).ready(function () {
    loadData();    
});